import turtle
import time
# 创建一个海龟画图对象并设定宽度
t = turtle.Turtle()
t.width(10)

# 绘制 "H"
t.penup()
t.goto(-150, 75)
t.pendown()
t.color('#755AFB')
t.right(90)
t.forward(150)
t.backward(75)
t.left(90)
t.forward(75)
t.left(90)
t.forward(75)
t.backward(150)

# 绘制 "o"
t.penup()
t.goto(0, 25)
t.setheading(180)
t.pendown()
t.color('#FCC72E')
t.circle(50)

# 绘制 "u"
t.penup()
t.goto(75, 30)
t.setheading(-90)
t.pendown()
t.color('#3183FA')
t.forward(50)
t.circle(50, 180)
t.forward(50)
#---------------------------------------
t.speed(0)
time.sleep(5)
t.penup()
t.goto(0,0)
t.width(2000)
t.color('#ae0002')
t.pendown()
t.goto(0.01,0)
t.penup()
t.goto(0,0)
t.color('white')
t.width(300)
t.pendown()
t.goto(0.01,0)
t.penup()
t.color('black')
t.width(40)
t.right(45)
for i in range(4):
    t.pendown()
    t.forward(90)
    t.right(90)
    t.forward(90)
    t.penup()
    t.goto(0,0)
turtle.done()