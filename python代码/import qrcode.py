import turtle
import time
t=turtle.Turtle()
t.penup()
t.goto(-120,120)
t.color('#ffff00')
t.width(30)
t.pendown()
t.forward(120)
t.right(90)
t.forward(40)
t.speed(5)
t.forward(120)
t.speed(4)
t.forward(80)
t.left(90)
t.forward(120)
t.speed(3)
t.left(180)
t.forward(120)
t.right(90)
t.forward(120)
t.left(90)
t.speed(2)
t.forward(88)
t.speed(1)
t.forward(32)
for i in range(24):
    t.left(3.75)
t.forward(58)    
time.sleep(0.847)
t.reset()
angle=0
t.width(30)
t.speed(0)
t.hideturtle()
t1=t.clone()
t2=t.clone()
t3=t.clone()
turtle.delay(0)
def move():
    t.fd(120)
    t1.fd(120)
    t2.fd(120)
    t3.fd(120)
def u():
    t.pu()
    t1.pu()
    t2.pu()
    t3.pu()
def d():
    t.pd()
    t1.pd()
    t2.pd()
    t3.pd()    
def r():
    t.rt(90)
    t1.rt(90)
    t2.rt(90)
    t3.rt(90)
def g():
    t.goto(0,0)
    t1.goto(0,0)
    t2.goto(0,0)
    t3.goto(0,0)
def c():
    t.clear()
    t1.clear()
    t2.clear()
    t3.clear()
def sh():
    t.setheading(angle)
    t1.setheading(angle+90)
    t2.setheading(angle+180)
    t3.setheading(angle+270)
c()
while True:
    sh()
    for i in range(4):
        d()
        move()
        r()
        move()
        u()
        g()
    c()
    angle-=15
    