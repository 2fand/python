import turtle

# 创建一个海龟画图对象并设定宽度
t = turtle.Turtle()
t.width(10)
t1=t.clone()
t2=t.clone()
t.shape('circle')
t1.shape('square')
t2.shape('triangle')
# 绘制 "H"
t.penup()
t.goto(-150, 75)
t.pendown()
t.color('#755AFB')
t.right(90)
t.forward(150)
t.backward(75)
t.left(90)
t.forward(75)
t.left(90)
t.forward(75)
t.backward(150)

# 绘制 "o"
t1.penup()
t1.goto(0, 25)
t1.setheading(180)
t1.pendown()
t1.color('#FCC72E')
t1.circle(50)

# 绘制 "u"
t2.penup()
t2.goto(75, 30)
t2.setheading(270)
t2.pendown()
t2.color('#3183FA')
t2.forward(50)
t2.circle(50, 180)
t2.forward(50)
t.pu()
t1.pu()
t2.pu()
t.goto(-175,150)
t1.goto(-175,0)
t2.goto(-175,-150)
t.setheading(0)
t1.setheading(0)
t2.setheading(0)
t.pd()
t1.pd()
t2.pd()
t.forward(500)
t1.forward(500)
t2.forward(500)
t.color('#FCC72E')
t1.color('#3183FA')
t2.color('#755AFB')
t.backward(500)
t1.backward(500)
t2.backward(500)
t.color('black')
t1.color('black')
t2.color('black')
t.write('PROJECT MOON',font=("Verdana", 30, "normal"))
t1.write('PROJECT MOON',font=("Arial", 30, "normal"))
t2.write('PROJECT MOON',font=("Tahoma", 30, "normal"))
# 结束绘制
turtle.done()